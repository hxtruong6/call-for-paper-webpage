                <li>Fraud detection and risk management using data analytics</li>
                <li>Text mining and sentiment analysis in financial markets</li>
                <li>Portfolio optimization and asset allocation strategies</li>
                <li>Machine learning applications in accounting, auditing and compliance</li>
                <li>Quantitative models of information disclosure for Corporate social responsibility (CSR);
                    environment, social and governance (ESG); Sustainability reporting (SR)</li>
                <li>Data analysis for contemporary issues in accounting and auditing</li>
                <li>Financial sentiment analysis using social media data</li>
                <li>Algorithmic trading and quantitative finance</li>
                <li>Big data analytics for financial decision-making</li>
                <li>Visualization techniques for financial data analysis</li>

            </ul>
